#!/bin/bash

# Inicializar un nuevo proyecto npm
npm init
# Instalar express y guardarlo como dependencia
npm install express --save
# Instalar multer y guardarlo como dependencia -- Permite el procesamiento y almacenamiento de archivos que se envían desde un formulario HTML en una solicitud HTTP.
npm install multer --save
# Instalar validator y guardarlo como dependencia -- Proporcionar métodos y utilidades para validar y manipular cadenas de texto de manera sencilla.
npm install validator --save
# Instalar cors y guardarlo como dependencia -- Proporciona middleware que puedes agregar a tu aplicación para configurar y manejar las solicitudes CORS.
npm install cors --save
# Instalar axios y guardarlo como dependencia -- Proporciona una interfaz fácil de usar para realizar solicitudes GET, POST, PUT, DELETE, entre otras, a través de HTTP o HTTPS.
npm install axios --save
# Instalar nodemon y guardarlo como dependencia de desarrollo
npm install nodemon --save-dev
# Instalar moment y guardarlo como dependencia -- es una herramienta popular para el manejo de fechas y tiempos. Proporciona una serie de funciones para analizar, manipular, formatear y mostrar fechas y tiempos de manera sencilla.
npm install moment --save
# Instalar bcrypt-nodejs y guardarlo como dependencia --  proporciona funciones para el hashing de contraseñas utilizando el algoritmo bcrypt
npm install bcrypt-nodejs --save
# Instalar JWT y guardarlo como dependencia -- La principal función de jwt-simple es facilitar la creación y manipulación de tokens JWT, que se utilizan para autenticar y autorizar solicitudes en aplicaciones web y servicios API.
npm install jwt-simple --save


#########   Bases de datos #############
#MongoDB
#proporciona una interfaz sencilla y basada en esquemas para interactuar con bases de datos MongoDB. Se utiliza comúnmente como una capa de abstracción para realizar operaciones CRUD (crear, leer, actualizar, eliminar) en MongoDB de manera más fácil y estructurada.
# Instalar mongoose y guardarlo como dependencia
npm install mongoose --save

# Añade el metodo de paginacion a mongoose
npm install mongoose-pagination --save


